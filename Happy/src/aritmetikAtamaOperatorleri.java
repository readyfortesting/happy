
public class aritmetikAtamaOperatorleri {

	public static void main(String[] args) {
		
		// Aritmetik Atama Operatorleri
		
		int sayi =20;
		
		sayi*=5; //(carp,atama yap) 100
		System.out.println(sayi);
		
		sayi+=5; //(Topla,atama yap) 105
		System.out.println(sayi);
		
		sayi-=5; // (Cikar, atama yap) 100
		System.out.println(sayi);
		
		sayi/=5; // Bol,atama yap) 20
		System.out.println(sayi);
		
		sayi%=5; // modul,atama yap) 0
		System.out.println(sayi);
	}

}
